/**
 * add - returns sum
 * @a: first operand
 * @b: second operand
 * Return: sum of a and b
 */

int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - returns difference
 * @a: first operand
 * @b: second operand
 * Return: difference of a and b
 */

int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - returns product
 * @a: first operand
 * @b: second operand
 * Return: product of a and b
 */

int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - returns integer division
 * @a: first operand
 * @b: second operand
 * Return: int division of a and b
 */

int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - returns modulo
 * @a: first operand
 * @b: second operand
 * Return: modulo of a and b
 */

int mod(int a, int b)
{
	return (a % b);
}
